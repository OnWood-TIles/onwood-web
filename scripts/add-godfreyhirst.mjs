// Scrapes the Godfrey Hirst AU carpet catalogue and inserts its ranges into
// lib/carpet.ts (brand "Godfrey Hirst"), so gen-carpet.mjs then emits them into
// carpet.json exactly like Feltex/Redbook. Godfrey Hirst owns the shared "gh"
// Cloudinary account, so the same carpetSwatchUrl(globalItemId) works.
//
// The search SSR only returns page 1 (client-paginated), so we pull each fibre-
// type facet (which each fit in one page) + the general page, and merge/dedupe by
// range - that captures far more than the 20 the default page shows.
// Idempotent (replaces the marked block). Run: node scripts/add-godfreyhirst.mjs
// then: node scripts/gen-carpet.mjs
import fs from "fs";
import path from "path";

const UA =
  "Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/120.0.0.0 Safari/537.36";
const START = "  // >>> Godfrey Hirst ranges (generated by scripts/add-godfreyhirst.mjs) >>>";
const END = "  // <<< end Godfrey Hirst ranges <<<";
const BASE = "https://www.godfreyhirst.com/au/product-search/carpet";
const SOURCES = [
  { url: `${BASE}/type/wool`, fibre: "Wool" },
  { url: `${BASE}/type/solution-dyed-nylon`, fibre: "Solution Dyed Nylon" },
  { url: `${BASE}/type/triexta`, fibre: "Triexta" },
  { url: `${BASE}/type/duratuft`, fibre: "Duratuft" },
  { url: BASE, fibre: "" }, // general - catches any range without a type facet
];

async function fetchProducts(url) {
  const r = await fetch(url, { headers: { "User-Agent": UA } });
  const h = await r.text();
  const m = h.match(
    /<script id="__NEXT_DATA__" type="application\/json">([\s\S]*?)<\/script>/,
  );
  return m ? JSON.parse(m[1]).props.pageProps.initialProducts || [] : [];
}

async function run() {
  const byRange = new Map(); // name -> { colours, fibre }
  for (const src of SOURCES) {
    const products = await fetchProducts(src.url);
    for (const p of products) {
      const range = (p.name || "").trim();
      if (!range) continue;
      const colours = (p.variants || [])
        .filter((v) => v.globalItemId && v.name)
        .map((v) => ({ name: v.name.trim(), id: String(v.globalItemId) }));
      if (!colours.length) continue;
      const cur = byRange.get(range);
      if (!cur) byRange.set(range, { colours, fibre: src.fibre });
      else if (!cur.fibre && src.fibre) cur.fibre = src.fibre; // fill fibre
    }
    console.log(`  ${src.fibre || "general"}: ${byRange.size} ranges so far`);
    await new Promise((r) => setTimeout(r, 400));
  }

  const ranges = [...byRange.entries()].sort((a, b) => a[0].localeCompare(b[0]));
  const totalColours = ranges.reduce((n, [, r]) => n + r.colours.length, 0);

  const lines = [START];
  for (const [range, { colours, fibre }] of ranges) {
    const cs = colours
      .map((col) => `c(${JSON.stringify(col.name)}, ${JSON.stringify(col.id)})`)
      .join(", ");
    lines.push(
      `  { name: ${JSON.stringify(range)}, category: "Wool Carpet", fibre: ${JSON.stringify(fibre || "Carpet")}, brand: "Godfrey Hirst", colours: [ ${cs} ] },`,
    );
  }
  lines.push(END);
  const block = lines.join("\n");

  const file = path.join(process.cwd(), "lib/carpet.ts");
  let src = fs.readFileSync(file, "utf8");
  const s = src.indexOf(START);
  if (s !== -1) {
    const e = src.indexOf(END, s);
    if (e !== -1)
      src = src.slice(0, s).replace(/\n[ \t]*$/, "\n") + src.slice(e + END.length + 1);
  }
  const boundary = src.indexOf("export type CarpetSwatchItem");
  const arrEnd = src.lastIndexOf("]", boundary);
  let before = src.slice(0, arrEnd).replace(/\s*$/, "");
  if (!before.endsWith(",")) before += ",";
  fs.writeFileSync(file, `${before}\n${block}\n${src.slice(arrEnd)}`);
  console.log(
    `inserted ${ranges.length} Godfrey Hirst ranges (${totalColours} colours) into lib/carpet.ts`,
  );
}
run();
